# Dictionaries are used to store data valus in KEY:VALUE pairs. 
# A dictionary is a collection which is ORDERED, CHANGEABLE, and do not allow duplicates(keys).
# Duplicate keys can't be added, but values can be of any data type and mutable.
# Dictionaries are written with curly brackets.

iDictionary = {
    "brand": "Ford",
    "model": "Mustang",
    "year": 1964
}

print(iDictionary)