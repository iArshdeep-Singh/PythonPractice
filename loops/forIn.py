# 'for' loop is used for iterating over a sequence(that is either a list, a tuple, a dictionary, a set, or a string)
# With 'for' loop, a set of statemnents can be executed, once for each item in a list, a tuple, a set etc.

x = ["add()", "insert()", "append()", "extend()", "remove()", "pop()", "copy()", "clear()", "index()", "reverse()", "sort()"]

for i in x:
    print(i)

print("\nend.")    